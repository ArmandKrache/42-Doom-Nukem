int		ft_sqrt(int nb)
{
	int x;

	if (nb <= 1)
		return (nb == 1 ? 1 : 0);
	x = 1;
	while (x++)
	{
		if (nb == x * x)
			return (x);
		if (nb < x * x)
			return (0);
	}
	return (0);
}
