char	*ft_strrev(char *str)
{
	int		x;
	int		y;
	char	c;

	x = 0;
	while (str[x])
		x++;
	x--;
	y = 0;
	while (x > y)
	{
		c = str[x];
		str[x] = str[y];
		str[y] = c;
		x--;
		y++;
	}
	return (str);
}
